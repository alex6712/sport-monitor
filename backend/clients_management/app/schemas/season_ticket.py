from uuid import UUID

from pydantic import BaseModel, Field


class SeasonTicketModel(BaseModel):
    """Полная модель абонемента клиента с детальной информацией.

    Содержит все данные об абонементе, включая его тип, срок действия
    и идентификационные данные. Используется для передачи полной информации
    о действующем или просроченном абонементе.

    Attributes
    ----------
    id : UUID
        Уникальный идентификатор абонемента в системе. Формат RFC 4122.
    type : str
        Тип абонемента, определяющий его условия и стоимость.
    expires_at : str
        Дата и время истечения срока действия абонемента в формате ISO 8601.
        Содержит временную зону (UTC по умолчанию).

    Notes
    -----
    - Поле `expires_at` возвращается в UTC временной зоне.
    - Для активных абонементов дата `expires_at` всегда в будущем.
    - Тип абонемента нельзя изменить после создания.
    - Просроченные абонементы сохраняются в истории клиента.
    - При автоматическом продлении создаётся новый абонемент с новым ID.
    """

    id: UUID = Field(examples=["8e1b38e9-b559-9f67-a2e8-a1839ee1d6a1"])
    type: str = Field(examples=["семейный"])
    expires_at: str = Field(examples=["2025-06-02 12:32:11.000311+00:00"])
